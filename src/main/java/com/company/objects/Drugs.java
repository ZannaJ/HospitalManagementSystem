package com.company.objects;

public class Drugs {
    private int id;
    private String name;

    public Drugs(){

    }
    public Drugs(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
