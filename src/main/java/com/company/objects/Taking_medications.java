package com.company.objects;

public class Taking_medications {
    private int id;
    private int quantity;

    public Taking_medications() {

    }
    public Taking_medications(int id, int quantity) {
        this.id = id;
        this.quantity = quantity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
